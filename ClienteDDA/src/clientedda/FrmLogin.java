    @Override
    public void Procesar(Mensaje msg) {
        if (msg.getEvento() == Evento.CREAR_NUEVA_PARTIDA) {
//            lstClientes.setListData(((ArrayList<Cliente>) msg.getMensaje()).toArray());
            Usuario u = (Usuario) msg.getAux();
            FrmPreJuego frm = new FrmPreJuego(u,fachada);
            
            frm.setVisible(true);
            this.setVisible(false);
            try {
                ControladorPrePartida cpp = new ControladorPrePartida(fachada, frm);
            } catch (RemoteException ex) {
                Logger.getLogger(FrmLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (msg.getEvento() == Evento.INGRESO_A_PARTIDA) {
            Partida p = (Partida) msg.getAux();
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog (null, "La apuesta inicial es de $" + (p.getPozo()/2) + ", Desea unirse?","Warning",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
              
                        FrmTablero frm = new FrmTablero((Partida) p);
                        frm.setTitle("Usuario: " + p.getJugadorDos().getNombreUsu() + "  Saldo Actual: " + p.getJugadorDos().getSaldo());
                        frm.setVisible(true);
                        this.setVisible(false);
                        try {
                            ControladorPartida cp = new ControladorPartida(frm,fachada, (Partida) p);
                        } catch (RemoteException ex) {
                            Logger.getLogger(FrmLogin.class.getName()).log(Level.SEVERE, null, ex);
                        }
            }
            
        }else if(msg.getEvento() == Evento.ADMINISTRADOR){
//            Usuario p = (Partida) msg.getAux();
            Usuario adm = (Usuario) msg.getAux();
            FrmAdministrador frm = new FrmAdministrador(fachada);
            frm.setTitle("Aministrador: " + adm.getNombreCompleto() );
            try {
                ControladorAdministrador ca = new ControladorAdministrador(fachada, frm);
            } catch (RemoteException ex) {
                Logger.getLogger(FrmPreJuego.class.getName()).log(Level.SEVERE, null, ex);
            }
            frm.setVisible(true);
        
        }else if (msg.getEvento() == Evento.PARTIDA_CREADA) {
//            LstPartidas.setListData(((ArrayList<Partida>) msg.getAux()).toArray());
            
            Partida p = (Partida) msg.getAux();
            FrmTablero frm = new FrmTablero(p);
            frm.setTitle("Usuario: " + p.getJugadorUno().getNombreUsu() + "  Saldo Actual: " + p.getJugadorUno().getSaldo());
            try {
                ControladorPartida cp = new ControladorPartida(frm, fachada, p);
            } catch (RemoteException ex) {
                Logger.getLogger(FrmPreJuego.class.getName()).log(Level.SEVERE, null, ex);
            }
            frm.setVisible(true);

        }else if (msg.getEvento() == Evento.SALDO_INSUFICIENTE){
            JOptionPane.showMessageDialog(null, "No tiene saldo suficiente para jugar");
        }else if (msg.getEvento() == Evento.USUARIO_YA_LOGUEADO){
            JOptionPane.showMessageDialog(null, "Este usuario ya esta logueado");
     }
    }

